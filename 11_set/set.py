# --------------set Methods (data type in python) defined in {}----------
sruthi = {1, 2, 3, 4, 5, 6, 7, 8, 9, 24}
print(sruthi)

# ------------------Add (to add some data into a set)-------------------
vamshi ={24, 4, 29, 30, 16}
vamshi.add(5)
print(vamshi)

# -----------------Update (to add bulk data)---------------------------
numbers= {12, 24, 4, 3, 6, 7, 9}
numbers.update({33, 55,66,88})
print(numbers)

# -----------------POP (to delete a random number)--------------------
bittu = {3, 5, 6, 8, 9, 10, }
bittu.pop()
print(bittu)

# -----------------Remove (to remove a particular element)------------
sandeep = {5, 23, 55, 98, 99, 66, 54, 77}
sandeep.remove(77)
print(sandeep)
